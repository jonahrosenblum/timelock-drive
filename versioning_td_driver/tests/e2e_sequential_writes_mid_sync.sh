analyze_md_amortization() {
    local analysis
    analysis="$(awk '
    /^\[gk_trace\] recv_write / {
        nb = 0; md = 0;
        for (i = 1; i <= NF; i++) {
            if ($i ~ /^num_data_ranges=/) nb = substr($i, 17) + 0;
            if ($i ~ /^num_md_blocks=/)   md = substr($i, 15) + 0;
        }
        total_writes++;
        total_md += md;
        if (md == 0) { zero_md_writes++; } else { nonzero_md_writes++; }
        pending_md = md;
        pending_ranges = nb;
        next;
    }
    /^\[gk_trace\] recv_range\[/ {
        blocks = 0;
        for (i = 1; i <= NF; i++) {
            if ($i ~ /^blocks=/) blocks = substr($i, 8) + 0;
        }
        if (pending_ranges > 0) {
            pending_data_blocks += blocks;
            pending_ranges--;
        }
        if (pending_ranges == 0 && pending_data_blocks > 0) {
            total_data_blocks += pending_data_blocks;
            if (pending_md > 0) {
                ratio = pending_data_blocks / pending_md;
                if (count_ratio == 0 || ratio < min_ratio) min_ratio = ratio;
                if (ratio > max_ratio) max_ratio = ratio;
                sum_ratio += ratio;
                count_ratio++;
            }
            pending_data_blocks = 0;
            pending_md = 0;
        }
        next;
    }
    END {
        printf "total_writes=%d total_data_blocks=%d total_md_blocks_sent=%d\n", total_writes, total_data_blocks, total_md;
        printf "writes_with_0_md=%d writes_with_md=%d\n", zero_md_writes, nonzero_md_writes;
        if (total_md > 0)
            printf "overall_data_blocks_per_md=%.2f\n", total_data_blocks / total_md;
        else
            printf "overall_data_blocks_per_md=inf (no md blocks sent)\n";
        if (count_ratio > 0)
            printf "per_write_data_blocks_per_md: mean=%.2f min=%.2f max=%.2f (over %d writes-with-md)\n", sum_ratio/count_ratio, min_ratio, max_ratio, count_ratio;
    }
    ' "$GATEKEEPER_LOG")"

    log "Metadata amortization report:"
    while IFS= read -r line; do
        log "  $line"
    done <<< "$analysis"
}

analyze_md_repeats() {
    local report
    report="$(awk '
    /^\[gk_trace\] recv_write / {
        in_write = 1;
        next;
    }
    in_write && /^\[gk_trace\] recv_md / {
        md = -1;
        for (i = 1; i <= NF; i++) {
            if ($i ~ /^md_idx=/) md = substr($i, 8) + 0;
        }
        if (md >= 0) {
            total_sends++;
            send_count[md]++;
        }
        next;
    }
    /^\[gk_trace\] recv_write / || !/^\[gk_trace\]/ { in_write = 0; }
    END {
        repeats = 0;
        for (md in send_count) {
            if (send_count[md] > 1) repeats++;
        }
        printf "distinct_md_indices_seen=%d md_indices_resent=%d total_md_sends=%d\n",
               length(send_count), repeats, total_sends;
        if (repeats > 0) {
            printf "resent_indices (md_idx: send_count):\n";
            for (md in send_count) {
                if (send_count[md] > 1)
                    printf "  md_idx=%d sent=%d\n", md, send_count[md];
            }
        }
    }
    ' "$GATEKEEPER_LOG")"

    log "Metadata repeat-send report:"
    while IFS= read -r line; do
        log "  $line"
    done <<< "$report"
}

